//*****************************************************************************
// グローバル変数
//*****************************************************************************
int g_Score;									// スコア

//=============================================================================
// スコアの初期化処理
//=============================================================================
void InitScore(void) {
	g_Score = 0;
}

//=============================================================================
// スコアの終了処理
//=============================================================================
void UninitScore(void) {

}

//=============================================================================
// スコアの更新処理
//=============================================================================
void UpdateScore(void) {

	// スコア上限を超えさせない
	if (g_Score++ >= 99999999) {
		g_Score = 99999999;
		return;
	}

	g_Score++;
}

//=============================================================================
// スコアの描画処理
//=============================================================================
void DrawScore(void) {
	
}

//=============================================================================
// スコアを取得
//=============================================================================
int GetScore(void) {
	return g_Score;
}